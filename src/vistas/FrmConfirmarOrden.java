    private void mostrarPlatillos(){
        float total = 0;
        this.setLocationRelativeTo(null);
        setResizable(false);
        pack();
        for (Platillo platillo : platillos) {
            txtPlatillos.append(platillo.toString() + "\n");
            total+=platillo.getCosto()*platillo.getCantidad();
        }
        
        txtPlatillos.append("-------------------------"
                + "\nTotal              $"+total);
        
    }
    
